import * as React from "react";

import { cn } from "@/lib/utils";

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    variant?: "default" | "glass" | "gradient" | "bordered";
    hover?: boolean;
  }
>(({ className, variant = "default", hover = true, ...props }, ref) => {
  const variants = {
    default: "rounded-xl border bg-card text-card-foreground shadow-sm",
    glass:
      "rounded-xl border bg-card/50 backdrop-blur-md text-card-foreground shadow-lg",
    gradient:
      "rounded-xl border-0 bg-gradient-to-br from-card via-card/95 to-muted/20 text-card-foreground shadow-lg",
    bordered: "rounded-xl border-2 bg-card text-card-foreground",
  };

  return (
    <div
      ref={ref}
      className={cn(
        variants[variant],
        hover &&
          "transition-all duration-300 hover:shadow-xl hover:-translate-y-1",
        "overflow-hidden relative",
        className
      )}
      {...props}
    />
  );
});
Card.displayName = "Card";

